package com.d5error.literal;

public class LiteralDemo {
    public static void main(String[] args) {
        // 整数
        System.out.println(666);
        // 小数
        System.out.println(2.2);
        // 字符
        System.out.println('s');
        // 字符串
        System.out.println("");
        // 布尔值
        System.out.println(true);
        System.out.println(false);
    }
}
